import SwiftUI

extension Color {
    static let brandColor = Color("brandColor")
    static let secondaryColor = Color("secondaryColor")
    static let categoryGreenColor = Color("categoryGreen")
    static let categoryPurpleColor = Color("categoryPurple")
    static let categoryRedColor = Color("categoryRed")
    static let discountRedColor = Color("discountRed")
    static let shadowColor = Color("shadow")
    static let secondaryTextColor = Color("secondaryText")
}

extension UIColor {
    static let brandColor = UIColor(named: "brandColor")
    static let secondaryColor = UIColor(named: "secondaryColor")
    static let categoryGreenColor = UIColor(named: "categoryGreen")
    static let categoryPurpleColor = UIColor(named: "categoryPurple")
    static let categoryRedColor = UIColor(named: "categoryRed")
    static let discountRedColor = UIColor(named: "discountRed")
    static let shadowColor = UIColor(named: "shadow")
    static let secondaryTextColor = UIColor(named: "secondaryText")
}
